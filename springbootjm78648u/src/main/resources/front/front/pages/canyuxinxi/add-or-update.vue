				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`canyuxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='yulemingcheng'){
						this.ruleForm.yulemingcheng = obj[o];
						this.ro.yulemingcheng = true;
						continue;
					}
					if(o=='xiangmuleixing'){
						this.ruleForm.xiangmuleixing = obj[o];
						this.ro.xiangmuleixing = true;
						continue;
					}
					if(o=='tupian'){
						this.ruleForm.tupian = obj[o].split(",")[0];
						this.ro.tupian = true;
						continue;
					}
					if(o=='suozaiweizhi'){
						this.ruleForm.suozaiweizhi = obj[o];
						this.ro.suozaiweizhi = true;
						continue;
					}
					if(o=='canyushijian'){
						this.ruleForm.canyushijian = obj[o];
						this.ro.canyushijian = true;
						continue;
					}
