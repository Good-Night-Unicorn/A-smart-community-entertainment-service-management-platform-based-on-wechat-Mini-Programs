	} from "@/utils/validate";
	export default {
		data() {
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					yulemingcheng : false,
					tupian : false,
					xiangmuleixing : false,
					canyurenshu : false,
					kaifangshijian : false,
					suozaiweizhi : false,
					zhuyishixiang : false,
					xiangqingjianjie : false,
					thumbsupnum : false,
					crazilynum : false,
					clicktime : false,
					clicknum : false,
					discussnum : false,
					storeupnum : false,
				},
			
				ruleForm: {
					yulemingcheng: '',
					tupian: '',
					xiangmuleixing: '',
					canyurenshu: Number('0'),
					kaifangshijian: '',
					suozaiweizhi: '',
					zhuyishixiang: '',
					xiangqingjianjie: '',
					clicktime: '',
				},
				xiangmuleixingOptions: [],

				rules: {
					yulemingcheng: [
						{ required: true, message: '娱乐名称不能为空', trigger: 'blur' },
					],
					tupian: [
					],
