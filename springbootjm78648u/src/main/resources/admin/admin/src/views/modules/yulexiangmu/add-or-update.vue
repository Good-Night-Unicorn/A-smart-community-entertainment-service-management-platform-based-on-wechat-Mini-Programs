			init(id,type) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='yulemingcheng'){
							this.ruleForm.yulemingcheng = obj[o];
							this.ro.yulemingcheng = true;
							continue;
						}
						if(o=='tupian'){
							this.ruleForm.tupian = obj[o];
							this.ro.tupian = true;
							continue;
						}
						if(o=='xiangmuleixing'){
							this.ruleForm.xiangmuleixing = obj[o];
							this.ro.xiangmuleixing = true;
							continue;
						}
						if(o=='canyurenshu'){
							this.ruleForm.canyurenshu = obj[o];
							this.ro.canyurenshu = true;
							continue;
						}
						if(o=='kaifangshijian'){
							this.ruleForm.kaifangshijian = obj[o];
							this.ro.kaifangshijian = true;
							continue;
						}
						if(o=='suozaiweizhi'){
							this.ruleForm.suozaiweizhi = obj[o];
							this.ro.suozaiweizhi = true;
							continue;
						}
						if(o=='zhuyishixiang'){
							this.ruleForm.zhuyishixiang = obj[o];
							this.ro.zhuyishixiang = true;
							continue;
						}
